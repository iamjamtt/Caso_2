    void cargarDatosReporte(String fec){
        int count = mpro.obtenerCountProducto();
        String[] a = new String[2];
        //a = mpro.obtenerCantidadDelDiaReportePorProducto("2022-06-23", 1);
 
        for (int i = 0; i < tablaReporte.getRowCount(); i++) {
            m.removeRow(i);
            i--;
        }
        
        m = (DefaultTableModel) tablaReporte.getModel();
        
        Object[] o = new Object[3];
        
        for (int i = 1; i <= count; i++) {
            a = mpro.obtenerCantidadDelDiaReportePorProducto(fec, i);
                
            System.out.println("Prod = " + a[0]);
            System.out.println("Cant = " + a[1]);
            
            o[0] = i;
            o[1] = a[0];
            o[2] = a[1];
            
            m.addRow(o);
            tablaReporte.setModel(m);
           
        }
        
        TableColumn t = tablaReporte.getColumn("NUM");
        t.setPreferredWidth(100);
        t.setMaxWidth(100);
        t.setMinWidth(100);
    }

